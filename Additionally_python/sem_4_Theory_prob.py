# Œº ‚Äî –º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–æ–µ –æ–∂–∏–¥–∞–Ω–∏–µ (—Å—Ä–µ–¥–Ω–µ–µ), 
# œÉ ‚Äî —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–æ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ
# –í–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å: –û—Å–Ω–æ–≤–Ω–∞—è –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å —Å–æ—Å—Ä–µ–¥–æ—Ç–æ—á–µ–Ω–∞ –æ–∫–æ–ª–æ —Å—Ä–µ–¥–Ω–µ–≥–æ –∑–Ω–∞—á–µ–Ω–∏—è Œº. 
# –ß–µ–º –¥–∞–ª—å—à–µ –æ—Ç —Å—Ä–µ–¥–Ω–µ–≥–æ, —Ç–µ–º –º–µ–Ω—å—à–µ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å. 
# –ë–æ–ª—å—à–∞—è —á–∞—Å—Ç—å –∑–Ω–∞—á–µ–Ω–∏–π (–æ–∫–æ–ª–æ 68%) –Ω–∞—Ö–æ–¥–∏—Ç—Å—è –≤ –ø—Ä–µ–¥–µ–ª–∞—Ö –æ–¥–Ω–æ–≥–æ —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–æ–≥–æ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏—è –æ—Ç Œº.
# –ü—Ä–∏–º–µ—Ä - –í—ã—Å–æ—Ç–∞ –ª—é–¥–µ–π, –æ—à–∏–±–∫–∏ –∏–∑–º–µ—Ä–µ–Ω–∏–π

# –†–∞–≤–Ω–æ–º–µ—Ä–Ω–æ–µ —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ - –ü—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∞—è, –≤—Å–µ –∑–Ω–∞—á–µ–Ω–∏—è –≤ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω–æ–º –∏–Ω—Ç–µ—Ä–≤–∞–ª–µ –∏–º–µ—é—Ç –æ–¥–∏–Ω–∞–∫–æ–≤—É—é –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å.
#—Å–ª—É—á–∞–π–Ω–∞—è –≤–µ–ª–∏—á–∏–Ω–∞ –º–æ–∂–µ—Ç —Å –æ–¥–∏–Ω–∞–∫–æ–≤–æ–π –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å—é –ø—Ä–∏–Ω–∏–º–∞—Ç—å –ª—é–±—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è –≤–Ω—É—Ç—Ä–∏ –∏–Ω—Ç–µ—Ä–≤–∞–ª–∞ [ùëé,ùëè],
# –∞ –≤–Ω–µ —ç—Ç–æ–≥–æ –∏–Ω—Ç–µ—Ä–≤–∞–ª–∞ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å —Ä–∞–≤–Ω–∞ –Ω—É–ª—é.
# –°–ª—É—á–∞–π–Ω–æ–µ —á–∏—Å–ª–æ –≤ –¥–∏–∞–ø–∞–∑–æ–Ω–µ, –≤—Ä–µ–º—è –æ–∂–∏–¥–∞–Ω–∏—è –≤ –ø—Ä–µ–¥–µ–ª–∞—Ö –∏–Ω—Ç–µ—Ä–≤–∞–ª–∞
# 
# 1. –°–ª—É—á–∞–π–Ω–∞—è –Ω–µ–ø—Ä–µ—Ä—ã–≤–Ω–∞—è –≤–µ–ª–∏—á–∏–Ω–∞ A –∏–º–µ–µ—Ç —Ä–∞–≤–Ω–æ–º–µ—Ä–Ω–æ–µ —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ –Ω–∞ –ø—Ä–æ–º–µ–∂—É—Ç–∫–µ (200, 800]. 
# –ù–∞–π–¥–∏—Ç–µ –µ–µ —Å—Ä–µ–¥–Ω–µ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –∏ –¥–∏—Å–ø–µ—Ä—Å–∏—é.
import math
from scipy.stats import norm

def math_expectation(a,b):
    M_x = (b+a)/2
    D_x = (b-a)**2/12
    return M_x, D_x

result_task_1 = math_expectation(200, 800)
print(f'—Å—Ä–µ–¥–Ω–µ–µ –∑–Ω–∞—á–µ–Ω–∏–µ, –ú(—Ö): {result_task_1[0]}, –¥–∏—Å–ø–µ—Ä—Å–∏—è D(—Ö): {result_task_1[1]}')               
# 2. –û —Å–ª—É—á–∞–π–Ω–æ–π –Ω–µ–ø—Ä–µ—Ä—ã–≤–Ω–æ–π —Ä–∞–≤–Ω–æ–º–µ—Ä–Ω–æ —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω–æ–π –≤–µ–ª–∏—á–∏–Ω–µ B –∏–∑–≤–µ—Å—Ç–Ω–æ, —á—Ç–æ –µ–µ –¥–∏—Å–ø–µ—Ä—Å–∏—è —Ä–∞–≤–Ω–∞ 0.2. 
# –ú–æ–∂–Ω–æ –ª–∏ –Ω–∞–π—Ç–∏ –ø—Ä–∞–≤—É—é –≥—Ä–∞–Ω–∏—Ü—É –≤–µ–ª–∏—á–∏–Ω—ã B –∏ –µ–µ —Å—Ä–µ–¥–Ω–µ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –∑–Ω–∞—è, —á—Ç–æ –ª–µ–≤–∞—è –≥—Ä–∞–Ω–∏—Ü–∞ —Ä–∞–≤–Ω–∞ 0.5? 
# –ï—Å–ª–∏ –¥–∞, –Ω–∞–π–¥–∏—Ç–µ –µ–µ.
a = 0.5
# 0.2 = (0.5 - b)**2/12, 
# 0.2*12 = (0.5 - b)**2, 
# 2,4 = (0.5 - b)
b = 0.5 + math.sqrt(2.4)
mu = (a+b)/2
print(f'–¥–∏–∞–ø–∞–∑–æ–Ω —Ä–∞–≤–Ω–æ–º–µ—Ä–Ω–æ–≥–æ —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è: [{a}:{b}]')
print(f'—Å—Ä–µ–¥–Ω–µ–µ –∑–Ω–∞—á–µ–Ω–∏–µ: {mu}')

# 3. –ù–µ–ø—Ä–µ—Ä—ã–≤–Ω–∞—è —Å–ª—É—á–∞–π–Ω–∞—è –≤–µ–ª–∏—á–∏–Ω–∞ X —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∞ –Ω–æ—Ä–º–∞–ª—å–Ω–æ –∏ –∑–∞–¥–∞–Ω–∞ –ø–ª–æ—Ç–Ω–æ—Å—Ç—å—é —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è 
# f(x) = (1 / (4 * sqrt(2pi))) * exp((-(x+2)**2) / 32)
# –ù–∞–π–¥–∏—Ç–µ:
# –∞). M(X)
Mx = -2
# –±). D(X)
Dx = 16
# –≤). std(X) (—Å—Ä–µ–¥–Ω–µ–µ –∫–≤–∞–¥—Ä–∞—Ç–∏—á–Ω–æ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ)
mean_sqr_deviation = math.sqrt(Dx)
print(f'—Å—Ä–µ–¥–Ω–µ–µ –∑–Ω–∞—á–µ–Ω–∏–µ (–º–∞—Ç. –æ–∂–∏–¥–∞–Ω–∏–µ): {Mx}')
print(f'–¥–∏—Å–ø–µ—Ä—Å–∏—è: {Dx}')
print(f'—Å—Ä–µ–¥–Ω–µ–µ –∫–≤–∞–¥—Ä–∞—Ç–∏—á–Ω–æ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ: {mean_sqr_deviation}')

# 4. –†–æ—Å—Ç –≤–∑—Ä–æ—Å–ª–æ–≥–æ –Ω–∞—Å–µ–ª–µ–Ω–∏—è –≥–æ—Ä–æ–¥–∞ X –∏–º–µ–µ—Ç –Ω–æ—Ä–º–∞–ª—å–Ω–æ–µ —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ.
# –ü—Ä–∏—á–µ–º, —Å—Ä–µ–¥–Ω–∏–π —Ä–æ—Å—Ç —Ä–∞–≤–µ–Ω 174 —Å–º, –∞ —Å—Ä–µ–¥–Ω–µ–µ –∫–≤–∞–¥—Ä–∞—Ç–∏—á–Ω–æ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ —Ä–∞–≤–Ω–æ 8 —Å–º.
# –ö–∞–∫–æ–≤–∞ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å —Ç–æ–≥–æ, —á—Ç–æ —Å–ª—É—á–∞–π–Ω—ã–º –æ–±—Ä–∞–∑–æ–º –≤—ã–±—Ä–∞–Ω–Ω—ã–π –≤–∑—Ä–æ—Å–ª—ã–π —á–µ–ª–æ–≤–µ–∫ –∏–º–µ–µ—Ç —Ä–æ—Å—Ç:
mu = 174
sigma = 8
x1 = 182
x2 = 190
x3 = 166
x4 = 158
x5 = 150
x6 = 198
# –∞). –±–æ–ª—å—à–µ 182 —Å–º
print(f'–≤–µ—Ä-—Ç—å, —á—Ç–æ —Ä–æ—Å—Ç —á–µ–ª–æ–≤–µ–∫–∞ –±–æ–ª—å—à–µ {x1}: {1 - norm.cdf((x1-mu)/sigma):.4f}')

# –±). –±–æ–ª—å—à–µ 190 —Å–º
print(f'–≤–µ—Ä-—Ç—å, —á—Ç–æ —Ä–æ—Å—Ç —á–µ–ª–æ–≤–µ–∫–∞ –±–æ–ª—å—à–µ {x2}: {1 - norm.cdf((x2-mu)/sigma):.4f}')

# –≤). –æ—Ç 166 —Å–º –¥–æ 190 —Å–º
print(f'–≤–µ—Ä-—Ç—å, —á—Ç–æ —Ä–æ—Å—Ç —á–µ–ª–æ–≤–µ–∫–∞ –±–æ–ª—å—à–µ {x3} –∏ –º–µ–Ω—å—à–µ {x2}: {(norm.cdf((x2-mu)/sigma)) - norm.cdf((x3-mu)/sigma):.4f}')

# –≥). –æ—Ç 166 —Å–º –¥–æ 182 —Å–º
print(f'–≤–µ—Ä-—Ç—å, —á—Ç–æ —Ä–æ—Å—Ç —á–µ–ª–æ–≤–µ–∫–∞ –±–æ–ª—å—à–µ {x3} –∏ –º–µ–Ω—å—à–µ {x1}: {(norm.cdf((x1-mu)/sigma) - norm.cdf((x3-mu)/sigma)):.4f}')

# –¥). –æ—Ç 158 —Å–º –¥–æ 190 —Å–º
print(f'–≤–µ—Ä-—Ç—å, —á—Ç–æ —Ä–æ—Å—Ç —á–µ–ª–æ–≤–µ–∫–∞ –≤—ã—à–µ {x4} –∏ –Ω–∏–∂–µ {x2}: {norm.cdf((x2-mu)/sigma) - norm.cdf((x4-mu)/sigma):.4f}')

# –µ). –Ω–µ –≤—ã—à–µ 150 —Å–º –∏–ª–∏ –Ω–µ –Ω–∏–∂–µ 190 —Å–º
print(f'–≤–µ—Ä-—Ç—å, —á—Ç–æ —Ä–æ—Å—Ç —á–µ–ª–æ–≤–µ–∫–∞ –Ω–∏–∂–µ {x5} –∏–ª–∏ –≤—ã—à–µ {x2}: {(1 -norm.cdf((x2-mu)/sigma)) + (norm.cdf((x5-mu)/sigma)):.4f}')

# —ë). –Ω–µ –≤—ã—à–µ 150 —Å–º –∏–ª–∏ –Ω–µ –Ω–∏–∂–µ 198 —Å–º
print(f'–≤–µ—Ä-—Ç—å, —á—Ç–æ —Ä–æ—Å—Ç —á–µ–ª–æ–≤–µ–∫–∞ –Ω–∏–∂–µ {x5} –∏–ª–∏ –≤—ã—à–µ {x6}: {(1- norm.cdf((x6-mu)/sigma)) + norm.cdf((x5-mu)/sigma):.4f}')

# –∂). –Ω–∏–∂–µ 166 —Å–º.
print(f'–≤–µ—Ä-—Ç—å, —á—Ç–æ —Ä–æ—Å—Ç —á–µ–ª–æ–≤–µ–∫–∞ –Ω–∏–∂–µ {x3}: {(norm.cdf((x3-mu)/sigma)):.4f}')



def normal_distribution(x, mu, sigma):
    z = (x - mu)/sigma
    a = 1 - norm.cdf(z)
    a1 = norm.cdf(z)
    return a, a1

mu = 174
sigma = 8
x_num = [182,190]
result_task_5 = [normal_distribution(x,mu, sigma) for x in x_num]
print(result_task_5)

print(f'–Ω–∏–∂–µ {x_num[0]}: {result_task_5[0][1]:.4f}')
print(f'–≤—ã—à–µ {x_num[1]}: {result_task_5[1][0]:.4f}')
print(f'–Ω–∏–∂–µ {x_num[1]}: {result_task_5[1][1]:.4f}')

def normal_distribution_range(y, mu, sigma):

    z = (y - mu)/sigma
    return norm.cdf(z)

mu = 174
sigma = 8
