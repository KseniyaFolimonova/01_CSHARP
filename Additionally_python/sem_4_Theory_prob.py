# 5. –ù–∞ —Å–∫–æ–ª—å–∫–æ —Å–∏–≥–º (—Å—Ä–µ–¥–Ω–∏—Ö –∫–≤–∞–¥—Ä–∞—Ç–∏—á–Ω—ã—Ö –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–π) –æ—Ç–∫–ª–æ–Ω—è–µ—Ç—Å—è —Ä–æ—Å—Ç —á–µ–ª–æ–≤–µ–∫–∞, —Ä–∞–≤–Ω—ã–π 190 —Å–º, 
# –æ—Ç –º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–æ–≥–æ –æ–∂–∏–¥–∞–Ω–∏—è —Ä–æ—Å—Ç–∞ –≤ –ø–æ–ø—É–ª—è—Ü–∏–∏, –≤ –∫–æ—Ç–æ—Ä–æ–π M(X) = 178 —Å–º –∏ D(X) = 25 –∫–≤.—Å–º?
